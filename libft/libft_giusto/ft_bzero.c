#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	size_t				i;
	unsigned char		*pt;

	i = 0;
	pt = (unsigned char *)str;
	while (i < n)
	{
		pt[i] = '\0';
		i++;
	}
}
