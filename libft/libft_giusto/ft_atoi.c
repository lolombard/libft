#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	segno;
	int	atoi;

	i = 0;
	segno = 1;
	atoi = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
		i++;
	if (str[i] == '-')
	{
		segno *= -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		atoi = (atoi * 10) + (str[i] - '0');
		i++;
	}
	return (atoi * segno);
}
